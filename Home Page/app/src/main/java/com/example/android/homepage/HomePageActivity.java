    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if ((id == R.id.profile)){
            Intent intent = new Intent(getBaseContext(), Profile.class);
            startActivity(intent);

        } else if (id == R.id.d_connect){
            // Handle the camera action
        } else if (id == R.id.d_explore) {

        } else if (id == R.id.d_home) {

        } else if (id == R.id.d_trending) {

        } else if (id == R.id.d_settings) {

        } else if (id == R.id.d_logout) {
            mAuth.signOut();
            Intent intentEmail = new Intent(HomePageActivity.this, PhoneAuthActivity.class);
            startActivity(intentEmail);

        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
